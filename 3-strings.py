my_string="Hola Mundo" 

print(my_string+" Que tal ")

print(my_string[0]) #Devuelve el primer caracter

print(my_string[-1]) #Devuelve el ultimo caracter

print(my_string[0:6]) #Devuelve de la posicion 0 hasta la 5

print(my_string[6]) #Devuelve de la posicion 6

print(my_string[-5:-1]) #Devuelve de la posicion -5 hasta -2

print(my_string[0:len(my_string):2]) #Devuelve de la posicion 0 hasta el final pero de 2 en 2

print(my_string[::-1]) #Devuelve el string de Derecha a Izquierda[Texto invertido]

